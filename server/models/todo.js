const mongoose = require('mongoose')
const Schema = mongoose.Schema

let todoSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    user: {
        type: Schema.Types.ObjectId,
        ref: 'User'
    },
    project: {
        type: Schema.Types.ObjectId,
        ref: 'Project'
    }
})

let Todo = mongoose.model('Todo', todoSchema)

module.exports = Todo